    private void loadAllCustomerId(){
        try {
            for(String id : CustomerController.getAllCustomerId()){
                cmbCustomerId.addItem(id);
            }
        } catch (ClassNotFoundException | SQLException ex) {
           JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
    
    private void loadAllItemCode(){
        try {
            for(String code : ItemController.getAllItemCode()){
                cmbItemCode.addItem(code);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
    
    private void setOrderId(){
        try {
            String id=OrderController.getLastOrderId();
            if(null!=id){
                id=id.split("[A-Z]")[1];
                id=String.format("D%03d",(Integer.parseInt(id)+1));
                lblOrderId.setText(id);
            }else{
                lblOrderId.setText("D001");
            }
        } catch (ClassNotFoundException | SQLException ex) {
           JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
    private int isAlreadyExitsRow(String code){
        DefaultTableModel dtm=(DefaultTableModel) tblOrder.getModel();
        for(int i=0; i<tblOrder.getRowCount(); i++){
            String temp=(String) dtm.getValueAt(i, 0);
            if(temp.equals(code)){
                return i;
            }
        }
        return -1;
    }
    private void calculateTotal(){
        DefaultTableModel dtm=(DefaultTableModel) tblOrder.getModel();
        
        double total=0;
        for(int i=0; i<tblOrder.getRowCount(); i++){
            total+=(double)dtm.getValueAt(i, 4);
        }
        lblTotal.setText(String.valueOf(total));
    }
