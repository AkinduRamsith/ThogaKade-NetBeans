    private void btnPlaceOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlaceOrderActionPerformed
        try {
            // TODO add your handling code here:
            String orderId=lblOrderId.getText();
            String customerId=cmbCustomerId.getSelectedItem().toString();
            String orderDate=lblDate.getText();
            ArrayList<OrderDetail> orderDetailList=new ArrayList<>();
            DefaultTableModel dtm=(DefaultTableModel) tblOrder.getModel();
            for(int i=0; i<tblOrder.getRowCount(); i++){
                String itemCode=(String)dtm.getValueAt(i, 0);
                int qty=(int)dtm.getValueAt(i, 2);
                double unitPrice=(double)dtm.getValueAt(i, 3);
                OrderDetail orderDetail=new OrderDetail(orderId, itemCode, qty, unitPrice);
                orderDetailList.add(orderDetail);
                
            }
            Order order=new Order(orderId, customerId, orderDate, orderDetailList);
            boolean isAdded=OrderController.placeOrder(order);
            if(isAdded){
                JOptionPane.showMessageDialog(this, "Added Success");
            }
        } catch (ClassNotFoundException | SQLException ex) {
             JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        
        
    }//GEN-LAST:event_btnPlaceOrderActionPerformed

